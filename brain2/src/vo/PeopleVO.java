package vo;

public class PeopleVO {
	private int seqNo;
	private String joinman;
	private int matchseqNo;
	private int flag;

	public int getSeqNo() {
		return seqNo;
	}

	public void setSeqNo(int seqNo) {
		this.seqNo = seqNo;
	}

	public String getJoinman() {
		return joinman;
	}

	public void setJoinman(String joinman) {
		this.joinman = joinman;
	}

	public int getMatchseqNo() {
		return matchseqNo;
	}

	public void setMatchseqNo(int matchseqNo) {
		this.matchseqNo = matchseqNo;
	}

	public int getFlag() {
		return flag;
	}

	public void setFlag(int flag) {
		this.flag = flag;
	}
}
